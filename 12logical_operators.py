# Logical Operators Tutorial AND , OR , NOT 

# Logical AND (and)
x = True
y = False

# Check if both x and y are True
print(x and y)  # Output: False

# Check if both x and y are True using variables
print(x and y)  # Output: False

# Logical OR (or)
a = True
b = False

# Check if either a or b is True
print(a or b)  # Output: True

# Check if either a or b is True using variables
print(a or b)  # Output: True

# Logical NOT (not)
p = True

# Check if p is not True
print(not p)  # Output: False

# Check if p is not True using variable
print(not p)  # Output: False
