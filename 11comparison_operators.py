# Comparison Operators Tutorial 
# Comparison operators can only output true or false 

# Equal to (==)
x = 5
y = 5

# Check if x is equal to y
print(x == y)  # Output: True

# Not equal to (!=)
a = 5
b = 7

# Check if a is not equal to b
print(a != b)  # Output: True

# Less than (<)
m = 10
n = 15

# Check if m is less than n
print(m < n)  # Output: True

# Less than or equal to (<=)
p = 20
q = 20

# Check if p is less than or equal to q
print(p <= q)  # Output: True
